from modori.extensions import mecab

class NLP:
	def __init__():
		pass

	@staticmethod
	def pos(text):
		result = mecab.pos(text)
		return result

	@staticmethod
	def nouns(text):
		result = mecab.nouns(text)
		return result

	@staticmethod
	def morphs(text):
		result = mecab.nouns(text)
		return result
